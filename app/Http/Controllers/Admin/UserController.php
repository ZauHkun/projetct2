     */
    public function store_permit(SetPermissionRequest $request,$id)
    {
        $user=User::whereId($id)->firstOrFail();
        $user->syncRoles($request->get('role'));
        return redirect(action('admin\UserController@edit_permit',$id))->with('status','Successfully edited');
    }

